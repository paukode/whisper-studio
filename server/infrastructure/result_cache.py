"""First-class store for full tool outputs that exceeded the prompt budget.

The chat budgeter (server/chat/budget.py) trims oversize tool results before
they reach the model; the FULL output is persisted here so nothing is lost.
Three consumers:

  - the model, via the always-available ``read_cached_result`` tool
    (server/executors/result_cache.py);
  - the user, via ``GET /api/result-cache/{filename}`` (router below) which
    the truncation toast links to;
  - retention, via GC-on-write: files older than ``RETENTION_DAYS`` are
    deleted whenever a new result is persisted.

Files live under ``<data_root>/result_cache/`` — resolved through
``server.infrastructure.paths.data_root`` so the location survives worktree
checkouts and can be pinned via env/config.
"""

import logging
import os
import re
import time

from fastapi import APIRouter
from fastapi.responses import PlainTextResponse

from server.infrastructure.paths import data_root

log = logging.getLogger("whisper-studio")

RETENTION_DAYS = 7

# Filenames are generated by write() as <tool>_<millis>.txt; accept exactly
# that shape (no separators, no traversal) when reading one back.
_SAFE_FILENAME = re.compile(r"^[A-Za-z0-9][A-Za-z0-9._-]*\.txt$")

# read() output cap, chars. Kept under the chat budgeter's 50KB so a cached
# slice fed back to the model never re-triggers truncation (which would
# pointlessly persist a cache file of a cache file).
_READ_CAP_CHARS = 48_000


def cache_dir() -> str:
    return os.path.join(data_root(), "result_cache")


def relative_path(filename: str) -> str:
    """The path shown in messages/UI — real and repo-relative."""
    return f"data/result_cache/{filename}"


def is_safe_filename(filename: str) -> bool:
    return (
        bool(_SAFE_FILENAME.match(filename))
        and os.path.basename(filename) == filename
        and ".." not in filename
    )


def _gc(directory: str) -> None:
    """Delete cache files older than RETENTION_DAYS. Best-effort per file."""
    cutoff = time.time() - RETENTION_DAYS * 86400
    try:
        entries = os.scandir(directory)
    except OSError:
        return
    removed = 0
    with entries:
        for entry in entries:
            try:
                if entry.is_file() and entry.stat().st_mtime < cutoff:
                    os.unlink(entry.path)
                    removed += 1
            except OSError:  # raced/permission — skip, retry next write
                continue
    if removed:
        log.info("result_cache GC: removed %d file(s) older than %dd", removed, RETENTION_DAYS)


def write(tool_name: str, output: str) -> str:
    """Persist a full tool output; returns the generated filename.

    Runs retention GC first so the cache never grows unbounded. Raises on
    write failure — the budgeter has its own degraded fallback.
    """
    directory = cache_dir()
    os.makedirs(directory, exist_ok=True)
    _gc(directory)
    safe_tool = re.sub(r"[^A-Za-z0-9_-]", "_", tool_name)[:64] or "tool"
    filename = f"{safe_tool}_{int(time.time() * 1000)}.txt"
    with open(os.path.join(directory, filename), "w", errors="replace") as f:
        f.write(output)
    return filename


def full_path(filename: str) -> str | None:
    """Validated absolute path for an existing cache file, else None."""
    if not is_safe_filename(filename):
        return None
    path = os.path.join(cache_dir(), filename)
    return path if os.path.isfile(path) else None


def read(filename: str, offset: int = 1, limit: int | None = None) -> str:
    """Read a cached result with 1-based line offset/limit, numbered like
    ws_read_file. Returns error strings (not raises) — this feeds the model."""
    if not is_safe_filename(filename):
        return f"Error: invalid cache filename: {filename!r}"
    path = os.path.join(cache_dir(), filename)
    if not os.path.isfile(path):
        return (
            f"Cached result not found: {filename}. It may have been removed by "
            f"the {RETENTION_DAYS}-day retention policy."
        )
    try:
        with open(path, errors="replace") as f:
            lines = f.readlines()
    except OSError as e:
        return f"Error reading cached result: {e}"
    total_lines = len(lines)
    start = max(offset, 1)
    selected = lines[start - 1 :]
    if limit is not None and limit > 0:
        selected = selected[:limit]
    numbered = [f"{start + i:>5}\t{line.rstrip()}" for i, line in enumerate(selected)]
    result = "\n".join(numbered)
    if len(result) > _READ_CAP_CHARS:
        result = result[:_READ_CAP_CHARS]
        result += (
            f"\n... [capped at ~48KB — file has {total_lines} lines; "
            f"re-call with offset/limit to read further]"
        )
    return result


# ── HTTP surface: the truncation toast links here ─────────────────────────────

router = APIRouter(prefix="/api/result-cache", tags=["result-cache"])


@router.get("/{filename}")
def get_cached_result(filename: str) -> PlainTextResponse:
    path = full_path(filename)
    if path is None:
        return PlainTextResponse("Cached result not found.", status_code=404)
    try:
        with open(path, errors="replace") as f:
            return PlainTextResponse(f.read())
    except OSError:
        return PlainTextResponse("Cached result unreadable.", status_code=500)
